Les postes, écrans et téléphone sont dans un local à proximité de notre bureau. Une fois que j’ai récupérer l’ensemble du matériel (câble réseau, souris, clavier, écran et téléphone) je vais installer le matériel dans le bureau de l’employé concerné, idéalement la veille pour que l’utilisateur puisse travailler dès son arrivé.
